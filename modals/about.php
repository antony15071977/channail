<p>АНАТОМИЧЕСКАЯ ФРЕЗА</p>
<p>Анатомическая фреза &ndash; новая трапецивидная форма фрезы с плоским донышком и абразивной боковой поверхностью. Чтобы понять отличие и плюсы новой фрезы от обычных, важно представить, как работают самые ходовые фрезы - шар и пламя. На высоких оборотах они вращаются вокруг своей оси, заворачивая эпонихий, кутикулу и птеригий воедино. Их округлая форма не позволяет зайти глубоко, что часто приводит к пропилам, недостаточной чистоте и даже крови.</p>
<p>В отличии от шара, пламени, анатомическая фреза работает отдельно с каждой пленкой: на низких оборотах (15000 оборотов/мин) анатомическую фрезу прикладывают горлышком к кутикуле на всю ее глубину и удерживают в каждой точке контакта в течении пары секунд. Горлышко шлифует внутреннюю поверхность кутикулы, донышко измельчает ороговевший слой птеригия, превратив его в мелкую тырсу, не повредив при этом эпонихий. Обработка начинается с правого синуса, а затем плавно возвратно-поступательными маятниковыми движениями по часовой стрелке фрезу перемещают к левому. Донышко фрезы на протяжении всей работы удерживают параллельно ногтевой пластине на расстоянии 0,01 мм, то есть мы не касаемся ногтевой пластины, что полностью исключает пропилы.</p>
<p>Работа анатомической фрезой позволяет перевести клиента на чистый аппарат за 3 сеанса, приостановить рост околоногтевой кожи, поменять анатомическое строение прилипшей кутикулы на красивый округлый валик, выйти на скорость 1 палец-1 минута.</p>
